package com.techvisio.einstitution.beans;

public class Subject {

	private Long id;
	private String subjectName;
	
	
	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	
	public String getQuotaCode() {
		return subjectName;
	}
	
	public void setQuotaCode(String quotaCode) {
		subjectName = quotaCode;
	}
	
}
